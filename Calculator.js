//Add function
function add(num1, num2){
    return num1 + num2;
}

//Minus function
function minus(num1, num2){
    return num1 - num2;
}

//Multiply function
function multiply(num1, num2){
    return num1 * num2;
}

//Divide function
function divide(num1, num2){
    return num1 / num2;
}

//Calculator function
function calculator(num1, num2, operrator){
    return operrator(num1, num2);
}


console.log(calculator(5, 4, add))









